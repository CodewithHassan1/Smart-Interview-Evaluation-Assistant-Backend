def _database_ssl_options():
    sslmode = os.getenv("DB_SSLMODE", "").strip()
    if sslmode:
        return {"sslmode": sslmode}
    host = os.getenv("DB_HOST", "")
    if host.endswith(".neon.tech"):
        return {"sslmode": "require"}
    return {}


